-- TOPLAM SATĨ TUTARI
SELECT SUM(OD.QUANTITY*OD.UNITPRICE) AS TOTALSALES
FROM ORDERDETAILS AS OD;

--TOPLAM SƯPARỮ SAYISI
SELECT COUNT(*) TOTALORDERS
FROM ORDERS;

--TOPLAM SATILAN ÜRÜN ADEDƯ
SELECT SUM(QUANTITY) TOTALQUANTITYSOLD
FROM ORDERDETAILS ;

-- ORTALAMA SƯPARỮ TUTARI
SELECT 
    AVG(ORDERTOTAL) AS AVERAGEORDERVALUE
FROM
(
    SELECT 
        O.ORDERID,
        SUM(OD.QUANTITY * OD.UNITPRICE) AS ORDERTOTAL
    FROM ORDERS O
    JOIN ORDERDETAILS OD ON O.ORDERID = OD.ORDERID
    GROUP BY O.ORDERID
)AS AV ;

--EN YÜKSEK TUTARLI SƯPARỮLER
SELECT 
    O.ORDERID,
    C.CUSTOMERNAME,
    O.ORDERDATE,
    SUM(OD.QUANTITY * OD.UNITPRICE) AS ORDERTOTAL
FROM ORDERS O
JOIN CUSTOMERS C ON O.CUSTOMERID = C.CUSTOMERID
JOIN ORDERDETAILS OD ON O.ORDERID = OD.ORDERID
GROUP BY O.ORDERID, C.CUSTOMERNAME, O.ORDERDATE
ORDER BY ORDERTOTAL DESC;

--EN ÇOK SATILAN ÜRÜNLER
SELECT 
    P.PRODUCTNAME,
    SUM(OD.QUANTITY) AS TOTALQUANTITYSOLD
FROM ORDERDETAILS OD
JOIN PRODUCTS P ON OD.PRODUCTID = P.PRODUCTID
GROUP BY P.PRODUCTNAME
ORDER BY TOTALQUANTITYSOLD DESC;

--EN ÇOK GELƯR GETƯREN ÜRÜNLER
SELECT 
    P.PRODUCTNAME,
    SUM(OD.QUANTITY * OD.UNITPRICE) AS TOTALREVENUE
FROM ORDERDETAILS OD
JOIN PRODUCTS P ON OD.PRODUCTID = P.PRODUCTID
GROUP BY P.PRODUCTNAME
ORDER BY TOTALREVENUE DESC;

-- KATEGORƯ BAZLI SATĨ ANALƯZƯ 
SELECT 
    CAT.CATEGORYNAME,
    SUM(OD.QUANTITY * OD.UNITPRICE) AS TOTALSALES
FROM ORDERDETAILS OD
JOIN PRODUCTS P ON OD.PRODUCTID = P.PRODUCTID
JOIN CATEGORIES CAT ON P.CATEGORYID = CAT.CATEGORYID
GROUP BY CAT.CATEGORYNAME
ORDER BY TOTALSALES DESC;

-- KATEGORƯ BAZLI SATILAN ÜRÜN ADEDƯ
SELECT 
    CAT.CATEGORYNAME,
    SUM(OD.QUANTITY) AS TOTALQUANTITY
FROM ORDERDETAILS OD
JOIN PRODUCTS P ON OD.PRODUCTID = P.PRODUCTID
JOIN CATEGORIES CAT ON P.CATEGORYID = CAT.CATEGORYID
GROUP BY CAT.CATEGORYNAME
ORDER BY TOTALQUANTITY DESC;

-- EN DEĐERLƯ MÜ̃TERƯLER
SELECT 
    C.CUSTOMERNAME,
    CI.CITYNAME,
    COUNT(DISTINCT O.ORDERID) AS ORDERCOUNT,
    SUM(OD.QUANTITY * OD.UNITPRICE) AS TOTALSPENT
FROM CUSTOMERS C
JOIN CITIES CI ON C.CITYID = CI.CITYID
JOIN ORDERS O ON C.CUSTOMERID = O.CUSTOMERID
JOIN ORDERDETAILS OD ON O.ORDERID = OD.ORDERID
GROUP BY C.CUSTOMERNAME, CI.CITYNAME
ORDER BY TOTALSPENT DESC;

--̃EHƯRLERE GÖRE SATĨ ANALƯZƯ
